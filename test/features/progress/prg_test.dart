      // First save progress
      await dao.upsert(
        connectionId: 1,
        filePath: '/music/to_clear.mp3',
        positionMs: 120000,
        durationMs: 300000,
      );

      // Verify it exists
      expect(await dao.find(1, '/music/to_clear.mp3'), isNotNull);

      // Simulate "清除播放进度" action
      await dao.delete(1, '/music/to_clear.mp3');

      // Verify record is gone
      final afterDelete = await dao.find(1, '/music/to_clear.mp3');
      expect(afterDelete, isNull,
          reason: '清除后数据库中不应再有该文件的进度记录');

      final count = await dao.count();
      expect(count, equals(0),
          reason: '表中不应有记录');
    });

