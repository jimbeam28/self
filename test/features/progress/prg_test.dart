      await dao.delete(1, '/music/visible.mp3');

      // After clear: progress gone → progress bar hidden
      final after = await dao.find(1, '/music/visible.mp3');
      expect(after, isNull,
          reason: '清除后进度记录不存在 → 进度条应消失');
    });

