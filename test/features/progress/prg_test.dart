/// SQL that mirrors the [play_progress] table definition in DatabaseHelper.
const _createTables = '''
  CREATE TABLE connections (
    id          INTEGER PRIMARY KEY AUTOINCREMENT,
    name        TEXT NOT NULL,
    url         TEXT NOT NULL,
    username    TEXT NOT NULL,
    password    TEXT NOT NULL,
    base_path   TEXT NOT NULL DEFAULT '/',
    is_active   INTEGER NOT NULL DEFAULT 0,
    created_at  INTEGER NOT NULL,
    updated_at  INTEGER NOT NULL
  );

  CREATE TABLE play_progress (
    id             INTEGER PRIMARY KEY AUTOINCREMENT,
    connection_id  INTEGER NOT NULL,
    file_path      TEXT NOT NULL,
    position_ms    INTEGER NOT NULL DEFAULT 0,
    duration_ms    INTEGER,
    last_played_at INTEGER NOT NULL,
    UNIQUE(connection_id, file_path),
    FOREIGN KEY(connection_id) REFERENCES connections(id) ON DELETE CASCADE
  );

  CREATE INDEX idx_progress_lookup
  ON play_progress(connection_id, file_path);
''';

/// Opens a fresh in-memory database, applies the schema, injects it into
/// [DatabaseHelper], and returns the handle so the test can close it.
Future<Database> _openTestDatabase() async {
  final db = await databaseFactoryFfi.openDatabase(inMemoryDatabasePath);
  // Execute each statement separately
  for (final stmt in _createTables.split(';')) {
    final trimmed = stmt.trim();
    if (trimmed.isNotEmpty) {
      await db.execute(trimmed);
    }
  }
  DatabaseHelper.instance.overrideDatabase(db);
  return db;
}

/// Helper: build a test [PlayProgress] with the given fields.
PlayProgress _progress({
  int? id,
  int connectionId = 1,
  String filePath = '/music/test.mp3',
  int positionMs = 30000,
  int? durationMs = 120000,
  DateTime? lastPlayedAt,
}) {
  return PlayProgress(
    id: id,
    connectionId: connectionId,
    filePath: filePath,
    positionMs: positionMs,
    durationMs: durationMs,
    lastPlayedAt: lastPlayedAt ?? DateTime(2024, 1, 15, 10, 0, 0),
  );
}

/// Compare two [PlayProgress] records, ignoring the `id` and `lastPlayedAt`
/// fields (which are generated by the DB).
void expectProgressEquals(
  PlayProgress actual,
  PlayProgress expected, {
  bool checkLastPlayedAt = false,
}) {
  expect(actual.connectionId, equals(expected.connectionId),
      reason: 'connectionId should match');
  expect(actual.filePath, equals(expected.filePath),
      reason: 'filePath should match');
  expect(actual.positionMs, equals(expected.positionMs),
      reason: 'positionMs should match');
  expect(actual.durationMs, equals(expected.durationMs),
      reason: 'durationMs should match');
  if (checkLastPlayedAt) {
    expect(actual.lastPlayedAt, equals(expected.lastPlayedAt),
        reason: 'lastPlayedAt should match');
  }
}

