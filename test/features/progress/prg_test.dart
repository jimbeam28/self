      // Logic verification: after delete, find() returns null,
      // so the UI's progressCheck becomes null and the progress bar hides.
      await dao.upsert(
        connectionId: 1,
        filePath: '/music/visible.mp3',
        positionMs: 60000,
        durationMs: 240000,
